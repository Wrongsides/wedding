create table WEDDING (
    ID VARCHAR2(36) not null,
    BRIDE_ID VARCHAR2(36) not null,
    GROOM_ID VARCHAR2(36) not null,
    VENUE_ID VARCHAR2(36) not null,
    SAVE_THE_DATE DATE
);

create table BRIDE (
    ID VARCHAR2(36) not null,
    FIRST_NAME VARCHAR2(150),
    MIDDLE_NAMES VARCHAR2(150),
    LAST_NAME VARCHAR2(150),
    ADDRESS_LINE_1 VARCHAR2(150),
    ADDRESS_LINE_2 VARCHAR2(150),
    ADDRESS_LINE_3 VARCHAR2(150),
    POSTCODE VARCHAR2(8)
);

create table GROOM (
    ID VARCHAR2(36) not null,
    FIRST_NAME VARCHAR2(150),
    MIDDLE_NAMES VARCHAR2(150),
    LAST_NAME VARCHAR2(150),
    ADDRESS_LINE_1 VARCHAR2(150),
    ADDRESS_LINE_2 VARCHAR2(150),
    ADDRESS_LINE_3 VARCHAR2(150),
    POSTCODE VARCHAR2(8)
);

create table VENUE (
    ID VARCHAR2(36) not null,
    NAME VARCHAR2(150),
    DESCRIPTION VARCHAR2(1000),
    ADDRESS_LINE_1 VARCHAR2(150),
    ADDRESS_LINE_2 VARCHAR2(150),
    ADDRESS_LINE_3 VARCHAR2(150),
    POSTCODE VARCHAR2(8)
);